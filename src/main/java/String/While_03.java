package String;

public class While_03 {

	public static void main(String[] args) {
		
		//Write a java program to print counting from 1 to 10 using while loop.
		
		int n = 0;
		
		output(n);
		
	}
	public static void output(int n) {
		while(n<=9) {
			n++;
			System.out.println(n);
		}
		
	}
}
