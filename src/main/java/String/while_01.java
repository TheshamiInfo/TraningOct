package String;

public class while_01 {
	public static void main(String[] args) {
		
		/* a java program to print “Hello, I am _” until n=50,
		the initial value of n=5 and n increment by 5 */ 
		
	int n = 5;
	Display(n);
	}

		public static void Display(int n) {
	
	while (n<=50) {
		System.out.println(" I Am Shami ");
		n+=5;
		
	}}}
		
	
		

